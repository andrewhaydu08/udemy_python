# Comparisons: > = (greater)
# < = (less than)
# >= = (greater or equal to)
# <= = (less than or equal to)
# == (is equal)
# ! = (different)


number = 5 > 2 +3
print(type(number))
print(number)

number = 5 == 2 +3
print(type(number))
print(number)

number = 5 >= 2 +3
print(type(number))
print(number)

number = 5 != 2 +3
print(type(number))
print(number)

list = [1, 2, 3, 4]
